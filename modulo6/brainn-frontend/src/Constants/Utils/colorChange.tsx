export const colorChange = (gameId: number) => {
    if( gameId == 0) {
         return "Color0"
     } else if (gameId == 1) {
         return "Color1"
     }
     else if (gameId == 2) {
         return "Color2"
     }
     else if (gameId == 3) {
         return "Color3"
     }
     else if (gameId == 4) {
         return "Color4"
     }
     else if (gameId == 5) {
         return "Color5"
     }
 } 